void LED_Tongle(uint16_t num, uint16_t time)
{
	uint16_t cnt=0;
	
	for(cnt=0; cnt<num*2; cnt++)
	{
		HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
		HAL_Delay(time);
	}
	HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_SET);
}

